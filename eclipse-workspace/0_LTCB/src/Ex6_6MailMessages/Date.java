package Ex6_6MailMessages;

public class Date {
	private int day, month,year;
	/**
	 * This is COnstructor of class Date
	 * @param day
	 * @param month
	 * @param year
	 */
	public Date(int day, int month, int year) {
		super();
		this.day = day;
		this.month = month;
		this.year = year;
	}
		/**
		 * Getter and Setter
		 * @return
		 */
		public int getDay() {
			return day;
		}
		public void setDay(int day) {
			this.day = day;
		}
		public int getMonth() {
			return month;
		}
		public void setMonth(int month) {
			this.month = month;
		}
		public int getYear() {
			return year;
		}
		public void setYear(int year) {
			this.year = year;
		}
		@Override
		public String toString() {
			return  day + "/" + month + "/" + year;
		}	
		
		
}
