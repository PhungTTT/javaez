package Ex4_1BankAccount;

public class Date {
	private int day;
	private int month;
	private int year;
	/**
	 * This is Constructor of Date
	 * Example:
	 * 	Date d1 = new Date(1,6,2005);
		Date d2 = new Date(13,12,2021);
	 */
	public Date(int day, int month, int year) {
		super();
		this.day = day;
		this.month = month;
		this.year = year;
	}
}
